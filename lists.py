power_rangers = ["Red", "Blue", "Yellow"]

power_rangers.append("Pink")
power_rangers.append("Black")
power_rangers.append("Green")

#pop is om weg te halen, weg poppen van een vaste locatie
power_rangers.pop() 
# reverse is om abc te zetten maar andersom
power_rangers.reverse()
# append is een toevoegen
power_rangers.append()
# sort is alfabetische volgorde
power_rangers.sort ()
#remove is voor een specifieke benoeming
power_rangers.remove("Green")

# len berekent hoeveel power_rangers er zijn. Dus value van power_rangers
number_of_rangers = len(power_rangers)

print(number_of_rangers)